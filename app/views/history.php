<section class="py-16 bg-white">
    <div class="max-w-4xl mx-auto px-4 sm:px-6 lg:px-8">
        <h2 class="text-3xl font-bold text-primary-900 mb-8 border-l-4 border-accent-500 pl-4">Brief History of the Institute</h2>
        <div class="prose prose-lg text-gray-700">
            <p>
                The Kikam Technical Institute is situated in Kikam in the Ellembelle District of the Western Region of Ghana off the Takoradi - Elubo highway between Asanta and Esiama. It was founded in <strong>February, 1960</strong> by the late <strong>Abraham Acquah</strong> (a Catechist of Kikam Methodist Church) with the name <strong>Acumen Commercial Technical School</strong> and situated in the heart of Kikam town.
            </p>
            <p>
                Upon release of land by the then Chief of Kikam, <strong>Nana Kerma Bertu II</strong>, the school was moved to its present location in <strong>1963</strong> and renamed <strong>Fathia Vocational Institute</strong> with three (3) Teachers and ninety (90) Students. In September, 1963 it was absorbed into the public system and its name changed to <strong>Kikam Technical Institute</strong>. It was under the administration of Takoradi Technical Institute, now Takoradi Technical University until 1965.
            </p>
            <p>
                It is a day/boarding mixed institution with boys dominating in the enrolment. It has a land area of 2.57 square meters with greater part of the land undeveloped. Kikam Technical Institute operated under the erstwhile Technical/Vocational Education Division of the Ghana Education Service. Currently the Institute operates under the auspices of the <strong>Ghana TVET Service</strong>.
            </p>
            <p>
                The Institute is also one of the three institutions in the country that have been selected by the Government for the <strong>World Bank Oil and Gas Capacity Building Project</strong>. Its State-of-the-Art workshops inure to the academic and professional training of students who currently are in the numbers of two thousand, five hundred and ninety (2,590) at least as at last academic year.
            </p>
        </div>
    </div>
</section>

